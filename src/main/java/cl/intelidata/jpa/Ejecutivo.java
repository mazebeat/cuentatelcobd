package cl.intelidata.jpa;

import java.io.Serializable;
import java.util.Date;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Dev-DFeliu
 */
@Entity
@Table(name = "ejecutivo")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Ejecutivo.findAll", query = "SELECT e FROM Ejecutivo e"),
    @NamedQuery(name = "Ejecutivo.findById", query = "SELECT e FROM Ejecutivo e WHERE e.id = :id"),
    @NamedQuery(name = "Ejecutivo.findByNombre", query = "SELECT e FROM Ejecutivo e WHERE e.nombre = :nombre"),
    @NamedQuery(name = "Ejecutivo.findByTelefono", query = "SELECT e FROM Ejecutivo e WHERE e.telefono = :telefono"),
    @NamedQuery(name = "Ejecutivo.findByCelular", query = "SELECT e FROM Ejecutivo e WHERE e.celular = :celular"),
    @NamedQuery(name = "Ejecutivo.findByEmail", query = "SELECT e FROM Ejecutivo e WHERE e.email = :email"),
    @NamedQuery(name = "Ejecutivo.findByUpdatedAt", query = "SELECT e FROM Ejecutivo e WHERE e.updatedAt = :updatedAt"),
    @NamedQuery(name = "Ejecutivo.findByCreatedAt", query = "SELECT e FROM Ejecutivo e WHERE e.createdAt = :createdAt"),
    @NamedQuery(name = "Ejecutivo.findByDeletedAt", query = "SELECT e FROM Ejecutivo e WHERE e.deletedAt = :deletedAt")})
public class Ejecutivo implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Column(name = "nombre")
    private String nombre;
    @Column(name = "telefono")
    private String telefono;
    @Column(name = "celular")
    private String celular;
    @Column(name = "email")
    private String email;
    @Column(name = "updated_at")
    @Temporal(TemporalType.TIMESTAMP)
    private Date updatedAt;
    @Column(name = "created_at")
    @Temporal(TemporalType.TIMESTAMP)
    private Date createdAt;
    @Column(name = "deleted_at")
    @Temporal(TemporalType.TIMESTAMP)
    private Date deletedAt;
    @OneToMany(mappedBy = "ejecutivoId")
    private List<Cliente> clienteList;

    public Ejecutivo() {
    }

    public Ejecutivo(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Date getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Date updatedAt) {
        this.updatedAt = updatedAt;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getDeletedAt() {
        return deletedAt;
    }

    public void setDeletedAt(Date deletedAt) {
        this.deletedAt = deletedAt;
    }

    @XmlTransient
    public List<Cliente> getClienteList() {
        return clienteList;
    }

    public void setClienteList(List<Cliente> clienteList) {
        this.clienteList = clienteList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Ejecutivo)) {
            return false;
        }
        Ejecutivo other = (Ejecutivo) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "cl.intelidata.jpa.Ejecutivo[ id=" + id + " ]";
    }
    
}
